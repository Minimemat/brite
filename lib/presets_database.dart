import 'package:flutter/material.dart';
import 'effects_database.dart';
import 'palettes_database.dart';

class Preset {
  final String name;
  final String description;
  final IconData icon;
  final List<String> categories;
  final String effectName;
  final List<String>? colors;
  final int? paletteId;
  final int? sx;
  final int? ix;
  final int? c1;
  final int? c2;
  final int? c3;
  final bool? o1;
  final bool? o2;
  final bool? o3;
  bool isSelected;

  Preset({
    required this.name,
    required this.description,
    required this.icon,
    required this.categories,
    required this.effectName,
    this.colors,
    this.paletteId,
    this.sx,
    this.ix,
    this.c1,
    this.c2,
    this.c3,
    this.o1,
    this.o2,
    this.o3,
    this.isSelected = false,
  });

  Map<String, dynamic> toSettings() {
    return {
      'effect': effectName,
      if (colors != null && colors!.isNotEmpty) 'color': colors![0],
      if (paletteId != null) 'palette': paletteId,
      if (sx != null) 'speed': sx,
      if (ix != null) 'intensity': ix,
      if (c1 != null) 'custom1': c1,
      if (c2 != null) 'custom2': c2,
      if (c3 != null) 'custom3': c3,
      if (o1 != null) 'option1': o1,
      if (o2 != null) 'option2': o2,
      if (o3 != null) 'option3': o3,
    };
  }

  String getEffect() {
    final effect = EffectsDatabase.effectsDatabase.firstWhere(
      (e) => e['name'].toString().toLowerCase() == effectName.toLowerCase(),
      orElse: () => {'name': 'Unknown'},
    );
    return effect['name'] as String;
  }

  String getColorsOrPalette() {
    if (colors != null && colors!.isNotEmpty) {
      String hexColor = colors![0].replaceAll('#', '');
      if (hexColor.length == 6) return '#$hexColor';
      return '#FFFFFF';
    }
    if (paletteId != null) {
      final palette = PalettesDatabase.palettesDatabase.firstWhere(
        (p) => p['id'] == paletteId,
        orElse: () => {'name': 'Unknown'},
      );
      return 'Palette: ${palette['name'] as String}';
    }
    return 'No Palette';
  }
}

